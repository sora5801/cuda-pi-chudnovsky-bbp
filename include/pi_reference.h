//  These strings are the known-correct digits of pi. They let the program PROVE
//  to itself (and to you) that the Chudnovsky and BBP computations are right, by
//  comparing freshly computed digits against an independent source.
//
//  PROVENANCE / HOW THEY WERE VERIFIED
//  -----------------------------------
//  * Decimal digits (3500 of them): generated on this machine by an INDEPENDENT
//    method -- Machin's formula  pi/4 = 4*arctan(1/5) - arctan(1/239)  evaluated
//    with Python's arbitrary-precision Decimal at 4030-digit working precision.
//    The first 1000 of these digits were additionally confirmed to have SHA-256
//        808b01bd3137f0fd50877c7ad44b2a97478666390780372803859749172292bd
//    matching the value independently cross-checked against published tables
//    (angio.net's Pi-Search, numberworld.org / Alexander Yee, piday.org). Because
//    Machin's formula is exact and its first 1000 digits agree with those public
//    sources, the full 3500-digit string is trustworthy.
//  * Hexadecimal digits: from David H. Bailey's BBP references and independent
//    hex expansions (e.g. Herong Yang's listing). pi in hex is
//        3.243F6A8885A308D313198A2E03707344A4093822299F31D0082EFA98EC4E6C89...
//
//  Both strings are the digits AFTER the radix point (the leading "3." / "3" is
//  implicit), so PI_REF_DEC[0] == '1' (the first fractional decimal digit) and
//  PI_REF_HEX[0] == '2' (the first fractional hex digit). PI_REF_DEC_LEN is how
//  many decimal digits we can compare against before falling back to slower
//  self-consistency checks for larger digit counts.
// ============================================================================

